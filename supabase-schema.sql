-- Server-only request limits and short-lived duplicate fingerprints.
create table if not exists public.request_rate_limits (
  key_hash text primary key check (char_length(key_hash) = 64),
  window_started_at timestamptz not null default now(),
  request_count integer not null default 1 check (request_count > 0),
  updated_at timestamptz not null default now()
);

create table if not exists public.submission_fingerprints (
  key_hash text primary key check (char_length(key_hash) = 64),
  created_at timestamptz not null default now()
);

alter table public.request_rate_limits enable row level security;
alter table public.submission_fingerprints enable row level security;
revoke all on table public.request_rate_limits from anon, authenticated;
revoke all on table public.submission_fingerprints from anon, authenticated;
grant select, insert, update, delete on table public.request_rate_limits to service_role;
grant select, insert, update, delete on table public.submission_fingerprints to service_role;

create or replace function public.check_request_rate_limit(
  p_key_hash text,
  p_max_requests integer,
  p_window_seconds integer
)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  allowed boolean;
begin
  if char_length(p_key_hash) <> 64 or p_max_requests < 1 or p_window_seconds < 1 then
    return false;
  end if;

  insert into public.request_rate_limits (key_hash, window_started_at, request_count, updated_at)
  values (p_key_hash, now(), 1, now())
  on conflict (key_hash) do update
  set
    request_count = case
      when public.request_rate_limits.window_started_at <= now() - make_interval(secs => p_window_seconds)
        then 1
      else public.request_rate_limits.request_count + 1
    end,
    window_started_at = case
      when public.request_rate_limits.window_started_at <= now() - make_interval(secs => p_window_seconds)
        then now()
      else public.request_rate_limits.window_started_at
    end,
    updated_at = now()
  returning request_count <= p_max_requests into allowed;

  return coalesce(allowed, false);
end;
$$;

create or replace function public.reserve_submission_fingerprint(
  p_key_hash text,
  p_window_seconds integer
)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  reserved_hash text;
begin
  if char_length(p_key_hash) <> 64 or p_window_seconds < 1 then
    return false;
  end if;

  delete from public.submission_fingerprints
  where key_hash = p_key_hash
    and created_at <= now() - make_interval(secs => p_window_seconds);

  insert into public.submission_fingerprints (key_hash)
  values (p_key_hash)
  on conflict do nothing
  returning key_hash into reserved_hash;

  return reserved_hash is not null;
end;
$$;

create or replace function public.release_submission_fingerprint(p_key_hash text)
returns void
language sql
security definer
set search_path = public
as $$
  delete from public.submission_fingerprints where key_hash = p_key_hash;
$$;

revoke all on function public.check_request_rate_limit(text, integer, integer) from public, anon, authenticated;
revoke all on function public.reserve_submission_fingerprint(text, integer) from public, anon, authenticated;
revoke all on function public.release_submission_fingerprint(text) from public, anon, authenticated;
grant execute on function public.check_request_rate_limit(text, integer, integer) to service_role;
grant execute on function public.reserve_submission_fingerprint(text, integer) to service_role;
grant execute on function public.release_submission_fingerprint(text) to service_role;

-- Private, link-based Client Rooms. Public visitors never access these tables
-- directly: clients communicate through the token-validating Edge Function.
create table if not exists public.client_rooms (
  id uuid primary key default gen_random_uuid(),
  enquiry_id bigint references public.enquiries(id) on delete cascade,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  last_activity_at timestamptz not null default now(),
  client_name text not null check (char_length(client_name) between 1 and 100),
  project_title text not null check (char_length(project_title) between 1 and 140),
  token_hash text not null unique check (char_length(token_hash) = 64),
  status text not null default 'active' check (status in (
    'active', 'waiting_client', 'waiting_studio', 'revision', 'approved', 'complete'
  )),
  archived boolean not null default false
);

